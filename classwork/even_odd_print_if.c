#include <stdio.h>

void main()
{
	int i;
	for (i = 0; i < 30; i++)
	{
		if (i <= 10)
		{
			if (i % 2)
			{
				printf("--------------------------------------------------    [%d]\n", i);
			} else {
				printf("   +++++++++++++++++++++++++++++++++++++++++++        [%d]\n",i);
			}
		} else if (i == 15) {
				printf("      ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^               [%d]\n", i);
		} else if (i >= 20 && !(i % 7)) {
				printf("         **************************                   [%d]\n", i);
		}
	}
}
