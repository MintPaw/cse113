#include <stdio.h>

void main()
{
	int i;
	for (i = 0; i <= 10; i++)
	{
		if (i % 2)
		{
			printf("--------------------------------------------------    [%d]\n", i);
		} else {
			printf("   +++++++++++++++++++++++++++++++++++++++++++        [%d]\n",i);
		}
	}

	for (i = 11; i <= 20; i++)
	{
		if (i == 15)
		{
			printf("      ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^               [%d]\n", i);
		}
	}

	for (i = 20; i <= 30; i++)
	{
		if (!(i % 7))
		{
			printf("         **************************                   [%d]\n", i);
		}
	}

}
